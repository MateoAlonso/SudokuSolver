
public class Solution {
	
	private int posx;
	private int posy;
	private int num;
	
	Solution(int posx, int posy, int num){
		posx = this.posx;
		posx = this.posy;
		posx = this.num;
		
	}

	public int getPosx() {
		return posx;
	}

	public int getPosy() {
		return posy;
	}

	public int getNum() {
		return num;
	}
	
	
	
}
