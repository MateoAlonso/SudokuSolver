
public class Num {
	private int num;
	private boolean canChange;
	public Num(int num, boolean canChange) {
		super();
		this.num = num;
		this.canChange = canChange;
	}
	public int getNum() {
		return num;
	}
	public boolean isCanChange() {
		return canChange;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public void setCanChange(boolean canChange) {
		this.canChange = canChange;
	}
	
	
	
}
